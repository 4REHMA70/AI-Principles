class UserInterface():
    def __init__(self, environment):
        # Initialize Pygame
        pygame.init()
        # Set time the screen updates (for FPS)
        self.clock = pygame.time.Clock()

        # Get assets
        self.ground_image_list = GROUND_IMAGES_LIST
        self.wall_image_list = WALL_IMAGE_LIST
        # Load the assets and scale down 0.5 original size
        self.scale_factor = SCALE_FACTOR
        self.grounds = self.multiple_assets_loading(self.ground_image_list, self.scale_factor)
        self.walls = self.multiple_assets_loading(self.wall_image_list, self.scale_factor)

        # Generate the maze
        # maze = Maze(rows=15, cols=15, space_step=None, seed=45, remove_lone_blocks=False)
        # environment = maze.generate_maze(rand=1)
        self.environment = environment
        # Set the size w&h of each asset image location
        self.width = self.grounds[0].get_width()
        self.height = self.grounds[0].get_height()
        # Set the size of the windows base on background assets and environment size
        self.size = (self.width * self.environment.shape[1], self.height * self.environment.shape[0])
        self.screen = pygame.display.set_mode(self.size)

    # Method for loading and scaling the assets into pygame
    def multiple_assets_loading(self, path_list, scale_factor):
        # Load the images
        image_load = [pygame.image.load(path) for path in path_list]
        # Scale the images
        scale_image_list = [
            pygame.transform.scale(img, (img.get_width() * scale_factor, img.get_height() * scale_factor)) for img in
            image_load]
        return scale_image_list

    # Main method for running pygame:
    # Set the matrix for ground image choices
    def run(self):
        self.ground_matrix = [[random.choice(self.grounds) for _ in range(self.environment.shape[1])] for _ in range(self.environment.shape[0])]
        while True:
            # Loop until click the close button to close window
            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    pygame.quit()
                    sys.exit()

            # Draw the grid
            for i in range(self.environment.shape[0]):
                for j in range(self.environment.shape[1]):
                    self.screen.blit(self.ground_matrix[i][j], [j * self.width, i * self.height])

            # Draw the walls on top of the ground if node is 1
            for i in range(self.environment.shape[0]):
                for j in range(self.environment.shape[1]):
                    if self.environment[i][j] == 1:
                        self.screen.blit(self.walls[0], [j * self.width, i * self.height])

            # Limit frames per second
            self.clock.tick(UI_FPS)

            # Update the screen rendering
            pygame.display.flip()

